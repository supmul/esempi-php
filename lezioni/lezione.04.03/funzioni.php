<?php

    /**
     * FUNZIONI MATEMATICHE
     */

    function somma( $a, $b ) {
        return $a + $b;
    }

    function differenza( $a, $b ) {
        return $a - $b;
    }

    function prodotto( $a, $b ) {
        return $a * $b;
    }

    function divisione( $a, $b ) {
        return $a / $b;
    }
